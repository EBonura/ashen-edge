function check_attacks()
 local ax0=facing>0 and px+3 or px-17
 local ax1=ax0+14
 local ay0,ay1=py+3,py+19
 local function h(x,y,m) return ax1>x-m and ax0<x+m+16 and ay1>y-m and ay0<y+m+16 end
 local tx0,ty0,tx1,ty1=ax0\16,ay0\16,thi(ax1),thi(ay1)
 for ty=ty0,ty1 do
  for tx=tx0,tx1 do
   local fl=tile_flag(tx,ty)
   if fl&12>0 then
    local k=ty*lvl_w+tx+1
    local c=mdat[2][k] mdat[2][k]=0
    if fl&8>0 then mkp(tx*16,ty*16,3).c=c
    else for i=1,3 do mkp(tx*16+8,ty*16+8,3) end end
   end
  end
 end
 for e in all(ents) do
  local t=e.type
  if t==7 then
   if h(e.x,e.y,8) then e.lit,e.frame=false,7
    local p=mkp(e.x+8,e.y+8,1) p.cl=8 p.vx=rnd(1)-.5 p.vy=rnd(1)-.5 end
  elseif t==3 and e.hp>0
   and e.state~="death" and e.inv_t==0 then
   if h(e.x,e.y,0) then
    ent_hurt(e,a_spd,a_sph)
   end
  elseif t\2==2 and e.hp>0
   and e.state~="death" and e.state~="sleep"
   and (t~=4 or e.state~="fdash") and e.inv_t==0 then
   local wb=t==4
   local hw=wb and 14 or 15
   if ax1>e.x-hw and ax0<e.x+hw
    and ay1>e.y-(wb and 24 or 28) and ay0<e.y then
    ent_hurt(e,wb and a_wbdt or a_hbd,wb and a_wbd or a_hbh)
   end
  end
 end
end
function mkp(x,y,s)
 local p={x=x,y=y,vx=rnd(s)-s/2,vy=-rnd(s)-1,age=0}
 add(parts,p) return p
end
function update_parts()
 for p in all(parts) do
  p.x+=p.vx p.y+=p.vy
  if not p.cl then
   if tile_flag(p.x\16,p.y\16)&9>0 then
    p.y-=p.vy p.vy*=-0.4 p.vx*=0.7
   end
   p.vy+=0.08
  end
  p.age+=1
  if p.c or p.cl then
   if p.age>30 then del(parts,p) end
  elseif p.age>15 and abs(p.x-px)<10 and abs(p.y-py-11)<10 then
   gems+=1 del(parts,p)
  elseif p.age>900 then del(parts,p)
  end
 end
end

