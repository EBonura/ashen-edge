function _update60()
 -- fade update
 if fade_d~=0 then
  fade_t+=1
  if fade_t>=4 then
   fade_t=0
   fade_v+=fade_d
   if fade_v>=8 then
    fade_v=8
    if gs==2 then reset_game() end gs=gs%2+1
    fade_d=-1
   elseif fade_v<=0 then
    fade_v,fade_d=0,0
   end
  end
 end
 if gs~=1 then
  if fade_d==0 and (btnp(4) or btnp(5)) then sfx(sfx_confirm) fade_d=1 end
  return
 end
 if plr_inv>0 then plr_inv-=1 end
 local lr=plr_inv>30 and 0 or btn(1) and 1 or btn(0) and -1 or 0

 -- -- input buffering --
 if btnp(2) then buf_jump=8 end
 if btnp(4) then buf_atk=8 end
 if btnp(5) then buf_sweep=8 end
 buf_jump=max(0,buf_jump-1)
 buf_atk=max(0,buf_atk-1)
 buf_sweep=max(0,buf_sweep-1)

 -- -- death --
 if state=="death" then
  tick_anim()
  death_t+=1
  if death_t>90 then fade_d=1 end
  update_ents()
  update_sprojs()
  update_parts()
  update_camera()
  return
 end

 -- -- state machine --
 local s=state
 local ba=buf_atk>0 or buf_sweep>0
 if lr~=0 and anim_done(10) and (s=="land" or (not combo_queued and (s=="attack" or (s=="sweep" and not air_atk)))) then
  if s~="land" then end_attack() end
  combo_idx=0 vx,facing=lr*1.5,lr set_anim(a_run,"run")
 elseif s=="idle" or s=="run" then
  -- walked off ledge (wait 4 frames)
  if not grounded and air_time>4 then
   set_anim(a_fall)
   state="fall"
  -- buffered jump
  elseif buf_jump>0 then
   do_jump()
  -- buffered z: start combo
  elseif buf_atk>0 then
   start_combo()
  -- buffered x: sweep
  elseif buf_sweep>0 then
   start_sweep()
  elseif lr~=0 then
   vx,facing=lr*1.5,lr
   if s~="run" then
    set_anim(a_run,"run")
   end
  else
   damp()
   if s~="idle" then
    set_anim(a_idle,"idle")
   end
  end

 elseif s=="jump" then
  air_control(lr)
  if ba then
   start_air_atk()
  elseif vy>=0 then
   set_anim(a_fall,"fall")
  end

 elseif s=="fall" then
  air_control(lr)
  if ba then
   start_air_atk()
  elseif grounded then
   -- check buffers before landing
   if buf_jump>0 then
    do_jump()
   else
    vx=0
    set_anim(a_land,"land")
   end
  end

 elseif s=="land" then
  vx=0
  if anim_done() then
   -- check buffered actions
   if buf_atk>0 then
    start_combo()
   elseif buf_sweep>0 then
    start_sweep()
   else
    set_anim(a_idle,"idle")
   end
  end

 elseif s=="attack" then
  apply_atk_drift()
  -- buffer combo input anytime
  if buf_atk>0
   and combo_idx<#combo_chain then
   combo_queued=true
   buf_atk=0
  end
  if anim_done() then
   end_attack()
   if combo_queued
    and combo_idx<#combo_chain then
    combo_idx+=1
    combo_queued=false
    set_anim(combo_chain[combo_idx])
    sa(combo_chain[combo_idx])
   else
    combo_idx=0
    set_anim(a_idle,"idle")
   end
  end

 elseif s=="sweep" then
  if air_atk then
   air_control(lr)
   if ba and anim_done() then
    start_air_atk()
    return
   elseif grounded then
    air_atk=false
    set_anim(a_land,"land")
   elseif anim_done() then
    air_atk=false
    set_anim(a_fall,"fall")
   end
  else
   apply_atk_drift()
   if anim_done() then
    end_attack()
    if ba then
     vx=0
     set_anim(a_xslice,"attack")
     sa(a_xslice)
     buf_atk,buf_sweep=0,0
     combo_idx=0
    else
     set_anim(a_idle,"idle")
    end
   end
  end

 end

 if grounded and (s=="idle" or s=="run") then safe_x,safe_y=px,py end
 -- -- physics --
 prev_by1=py+19
 if not grounded then
  vy=min(vy+0.15,3)
  air_time+=1
 else
  air_time=0
 end

 -- move X with wall collision
 try_move_x(vx)

 -- move Y, then resolve Y collisions
 py+=vy
 resolve_y()

 -- fallback: bottom of map
 if py+19>=lvl_h*16 then land_on(lvl_h*16) end

 -- clamp to map bounds
 px=mid(3,px,lvl_w*16-4)

 -- hazard tiles
 if tile_flag(px\16,(py+19)\16)&16>0 then hurt_plr() end

 -- attack hitbox vs destructibles + entities
 if state=="attack" or state=="sweep" then
  check_attacks()
 end

 -- update entities & particles
 update_ents()
 update_sprojs()
 update_parts()

 -- update camera
 update_camera()

 -- tick animation
 tick_anim()
end


