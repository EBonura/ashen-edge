   e.inv_t,e.move_acc=0,0
   e.patrol_t,e.walk_count=60,0
  elseif t==4 then init_bot(e,a_wbi,4)
  elseif t==5 then init_bot(e,a_hbi,5)
  elseif t>=6 then
   e.x,e.y=e.tx*16,e.ty*16
   if t==6 then
    e.x+=8 e.y+=16
    e.anim,e.frame=a_ptl,1
