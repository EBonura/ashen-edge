function draw_char(a,f,sx,sy,flip,rot,rm)
 local buf,bx,by,bw,bh=unpack(acache[a].frames[f])
 if bw==0 then return end
 local cw=acache[a].cw
 local idx=1
 for y=0,bh-1 do
  for x=0,bw-1 do
   local col=ord(buf,idx)
   if rm then col=rm[col] or col end
   if col~=trans then
    local dx,dy=flip and cw-1-bx-x or bx+x,by+y
    if rot==1 then
     dx,dy=cw-1-dy,dx
