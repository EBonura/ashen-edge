function load_tiles()
 local sz=lvl_w*lvl_h
 if lvl_nt==0 then
  for L=1,lvl_nl do
   mdat[L]={}
   for i=1,sz do mdat[L][i]=0 end
  end
  return
 end
 local nt,nl=peek(map_base,2)
 local p=map_base+12+nl*2
 local tbpp=peek(p)
 local tnp=1<<tbpp
 local tpal={}
 for i=0,tnp-1 do
  local tb=peek(p+1+i\2)
  local c=i%2==0 and(tb>>4)&0xf or tb&0xf
  tpal[i]=c
 end
 local sp=p+1+tnp\2 local ss=pk2(sp) sp+=2
 local function wt(off,n,pal,bg)
  local b=decode_eg2(off,n*256,tbpp,16) local ai=1
  for t=0,n-1 do for py=0,15 do
   local d=bg and 0x8000+t*128+py*8 or(t\8*16+py)*64+t%8*8
   for px=0,7 do
    poke(d+px,(pal[b[ai]]&0xf)|(pal[b[ai+1]]&0xf)<<4) ai+=2
   end
  end end
 end
 wt(sp,lvl_nst,tpal) wt(sp+ss,nt-lvl_nst,tpal,1)
 p+=pk2(map_base+10)
 for L=1,nl do
  local lsz=pk2(map_base+10+L*2)
  local lbpp=peek(p)
  mdat[L]=decode_eg2(p+1,sz,lbpp,lvl_w)
  p+=lsz
 end
 local ne=peek(p) p+=1
 for i=1,ne do
  local a,b,c,d=peek(p,4)
  local e={type=a,tx=b,ty=c,group=d}
  p+=4
  if a==2 or a==6 then
   e.cost=peek(p) p+=1
  elseif a==8 then
   e.tw=peek(p) e.th=peek(p+1) p+=2
  end
  add(ents,e)
  ent_grp[d]=ent_grp[d] or {}
  add(ent_grp[d],#ents)
 end
end

function thi(v) return (v-1)\16 end

function tile_flag(tx,ty)
 if tx<0 or tx>=lvl_w or ty<0 or ty>=lvl_h then return 0 end
 local t=mdat[2][ty*lvl_w+tx+1]\4
 if t==0 then return 0 end
 return tflg[t] or 0
end

function tile_solid(tx,ty)
 return tile_flag(tx,ty)&9>0 or ent_solid(tx,ty)
end

function box_hits_solid(bx0,by0,bx1,by1)
 local tx0,ty0,tx1,ty1=bx0\16,by0\16,thi(bx1),thi(by1)
 for ty=ty0,ty1 do
  for tx=tx0,tx1 do
   if tile_solid(tx,ty) then
    return true
   end
  end
 end
end

