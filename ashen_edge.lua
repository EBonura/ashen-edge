function resolve_y()
 local bx0,by0,bx1,by1=px-3,py+3,px+3,py+19
 if box_hits_solid(bx0,by0,bx1,by1) then
  if vy>=0 then
   land_on(thi(by1)*16)
  elseif vy<0 then
   py,vy=(by0\16+1)*16-3,0
  end
  return
 end
 if vy>=0 then
  local tx0,tx1,ty=bx0\16,thi(bx1),by1\16
  local ptop=ty*16
  if prev_by1<=ptop then
   for tx=tx0,tx1 do
    if tile_flag(tx,ty)&2>0 then
     land_on(ptop)
     return
    end
   end
  end
 end
 grounded=false
end

function tr(cx,cy)
 local x,y=max(0,cx\16),max(0,cy\16)
 return x,y,min(lvl_w-1,x+8),min(lvl_h-1,y+8)
end

